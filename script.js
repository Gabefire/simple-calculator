/* Operator Functions */

function addNums(num1, num2) {
    finalNum = num1 + num2;
    return finalNum;
}

function subtractNums (num1, num2) {
    finalNum = num1 - num2;
    return finalNum;
}

function dividenums (num1, num2) {
    finalNum = num1 / num2;
    return finalNum;
}

function multiplyNums (num1, num2) {
    finalNum = num1 * num2;
    return finalNum;
}

function getPercent (num) {
    finalNum = num / 100;
    return finalNum;
}

function getNegative (num) {
    finalNum = -1 * num
    return finalNum
}